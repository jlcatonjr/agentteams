"""Inspect AI adapter (Cluster A Phase 2, increment 2).

Translates the framework-neutral eval-suite (``eval_suite.build_eval_suite``)
into a runnable Inspect AI task module, emitted as **source text**. This
module imports no ``inspect_ai`` — the framework import exists only inside the
generated string, so agentteams never takes an Inspect AI dependency and
``eval_suite.py`` stays framework-neutral.

The generated module evaluates the four neutral predicate kinds
(``frontmatter-list-contains-all``, ``agent-count``, ``handoff-chain``,
``frontmatter-and-body``) structurally against a generated team directory
given by the ``AGENTTEAMS_TEAM_DIR`` environment variable.
"""

from __future__ import annotations

import re
from pathlib import Path
from typing import Any

# Fixed preamble: helpers + a structural scorer + a no-op solver. The only
# `inspect_ai` import in this whole module is inside this generated text.
_PREAMBLE = '''\
# AUTO-GENERATED by agentteams.eval_adapters.inspect_ai — do not edit by hand.
# Regenerate from the framework-neutral eval-suite instead.
#
# Usage:
#   export AGENTTEAMS_TEAM_DIR=/path/to/<team>/.github/agents   (or .claude/agents)
#   inspect eval this_module.py
import os
from pathlib import Path

import yaml
from inspect_ai import Task, task
from inspect_ai.dataset import Sample
from inspect_ai.solver import solver
from inspect_ai.scorer import scorer, Score, Target, accuracy, mean, CORRECT, INCORRECT

_TEAM_DIR = Path(os.environ.get("AGENTTEAMS_TEAM_DIR", "."))


def _frontmatter(path):
    txt = Path(path).read_text(encoding="utf-8")
    if txt.startswith("---"):
        parts = txt.split("---", 2)
        if len(parts) == 3:
            try:
                data = yaml.safe_load(parts[1]) or {}
            except Exception:
                data = {}
            return (data if isinstance(data, dict) else {}), parts[2]
    return {}, txt


def _check(pred):
    kind = pred.get("kind")
    base = _TEAM_DIR
    if kind == "frontmatter-list-contains-all":
        f = base / pred["file"]
        if not f.exists():
            return False
        data, _ = _frontmatter(f)
        have = set(data.get(pred["field"], []) or [])
        return set(pred.get("values", [])).issubset(have)
    if kind == "agent-count":
        return len(list(base.glob("*" + pred["suffix"]))) == pred["count"]
    if kind == "handoff-chain":
        chain = pred.get("chain", [])
        for a in chain:
            if not (base / (a + ".agent.md")).exists():
                return False
        rt = pred.get("returns_to")
        if rt and chain:
            _, body = _frontmatter(base / (chain[-1] + ".agent.md"))
            if rt.lower() not in body.lower():
                return False
        return True
    if kind == "frontmatter-and-body":
        f = base / pred["file"]
        if not f.exists():
            return False
        data, body = _frontmatter(f)
        have = set(data.get("agents", []) or [])
        if not set(pred.get("agents_contains_all", [])).issubset(have):
            return False
        return pred.get("body_contains", "") in body
    return False


@scorer(metrics=[accuracy(), mean()])
def structural_scorer():
    async def score(state, target):
        pred = state.metadata["predicate"]
        ok = _check(pred)
        return Score(value=CORRECT if ok else INCORRECT, answer=str(ok))
    return score


@solver
def noop_solver():
    async def solve(state, generate):
        return state
    return solve

'''

_TASK_TEMPLATE = '''\

@task(name={id!r})
def {ident}():
    return Task(
        dataset=[Sample(
            input={claim!r},
            target="True",
            metadata={{"predicate": {predicate!r}}},
        )],
        solver=[noop_solver()],
        scorer=structural_scorer(),
    )
'''


def _safe_ident(scenario_id: str) -> str:
    ident = re.sub(r"\W+", "_", scenario_id).strip("_")
    if not ident or ident[0].isdigit():
        ident = "eval_" + ident
    return ident


def render_inspect_ai_module(suite: dict[str, Any]) -> str:
    """Return Inspect AI task-module source for a framework-neutral *suite*.

    Pure. One ``@task`` per scenario; predicate logic is the embedded
    ``_check`` interpreter. ``suite`` is consumed read-only and unmodified.
    """
    parts = [_PREAMBLE]
    seen: set[str] = set()
    for sc in suite.get("scenarios", []):
        ident = _safe_ident(sc["id"])
        while ident in seen:
            ident += "_x"
        seen.add(ident)
        parts.append(_TASK_TEMPLATE.format(
            id=sc["id"],
            ident=ident,
            claim=sc["claim"],
            predicate=sc["predicate"],
        ))
    return "".join(parts)


def write_inspect_ai_module(suite: dict[str, Any], path: Path | str) -> Path:
    """Write the rendered module to *path*. Thin wrapper over the pure renderer."""
    p = Path(path)
    p.parent.mkdir(parents=True, exist_ok=True)
    p.write_text(render_inspect_ai_module(suite), encoding="utf-8")
    return p


__all__ = ["render_inspect_ai_module", "write_inspect_ai_module"]
